package com.qianfeng.manager.bean;

public class ContactinfoBean {

	private String contact_id;
	
	private String contact_order;
	
	private String contact_name;
	
	private String contact_phone;
	
	private String contact_mail;
	
	private String contact_explain;

	public String getContact_id() {
		return contact_id;
	}

	public void setContact_id(String contact_id) {
		this.contact_id = contact_id;
	}

	public String getContact_order() {
		return contact_order;
	}

	public void setContact_order(String contact_order) {
		this.contact_order = contact_order;
	}

	public String getContact_name() {
		return contact_name;
	}

	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}

	public String getContact_phone() {
		return contact_phone;
	}

	public void setContact_phone(String contact_phone) {
		this.contact_phone = contact_phone;
	}

	public String getContact_mail() {
		return contact_mail;
	}

	public void setContact_mail(String contact_mail) {
		this.contact_mail = contact_mail;
	}

	public String getContact_explain() {
		return contact_explain;
	}

	public void setContact_explain(String contact_explain) {
		this.contact_explain = contact_explain;
	}
	
}
