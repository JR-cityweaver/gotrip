package com.qianfeng.goutrip.zcy.bean;

public class LimitDTO {

	private Integer limit_id;
	
	private String limit_date;
	
	private Integer limit_count;
	
	private Integer limit_detail;

	public Integer getLimit_id() {
		return limit_id;
	}

	public void setLimit_id(Integer limit_id) {
		this.limit_id = limit_id;
	}

	public String getLimit_date() {
		return limit_date;
	}

	public void setLimit_date(String limit_date) {
		this.limit_date = limit_date;
	}

	public Integer getLimit_count() {
		return limit_count;
	}

	public void setLimit_count(Integer limit_count) {
		this.limit_count = limit_count;
	}

	public Integer getLimit_detail() {
		return limit_detail;
	}

	public void setLimit_detail(Integer limit_detail) {
		this.limit_detail = limit_detail;
	}
}
